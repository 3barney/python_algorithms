# Set Creation
set1 = {'My', 'Set'}
print(set1)

# from list
set2 = set(['My', 'Set'])
print(set2)

# from dict
set3 = set({'my': 'My', 'set': 'Set'})
print(set3)

# from tuple
set4 = set(('My', 'Set'))
print(set4)

